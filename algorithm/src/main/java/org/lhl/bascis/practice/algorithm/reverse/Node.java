package org.lhl.bascis.practice.algorithm.reverse;

/**
 * 节点.
 * @author lunhengle
 */
public class Node {
    private int index;
    private Node next;

    public Node getNext() {
        return next;
    }

    public void setNext(Node next) {
        this.next = next;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }
}
